  let currentLang = localStorage.getItem('lang') || 'ko';

  function applyTranslations(lang) {
    document.documentElement.lang = lang;
    const t = TRANSLATIONS[lang];
    document.querySelectorAll('[data-i18n]').forEach(el => { const k = el.dataset.i18n; if (t[k] !== undefined) el.textContent = t[k]; });
    document.querySelectorAll('[data-i18n-html]').forEach(el => { const k = el.dataset.i18nHtml; if (t[k] !== undefined) el.innerHTML = t[k]; });
    document.querySelectorAll('[data-i18n-content]').forEach(el => { const k = el.dataset.i18nContent; if (t[k] !== undefined) el.setAttribute('content', t[k]); });
  }

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(e => { if (e.isIntersecting) e.target.classList.add('visible'); });
  }, { threshold: 0.1 });
  document.querySelectorAll('.animate-on-scroll').forEach(el => observer.observe(el));

  function pricingHint(p, lang) {
    const parts = [];
    if (p.download) parts.push(p.download === '무료' ? (lang === 'en' ? 'Free' : '무료') : p.download);
    if (p.monthly) parts.push(lang === 'en' ? `${p.monthly}/mo` : `월 ${p.monthly}`);
    if (p.yearly) parts.push(lang === 'en' ? `${p.yearly}/yr` : `연 ${p.yearly}`);
    if (p.lifetime) parts.push(lang === 'en' ? `${p.lifetime} lifetime` : `평생 ${p.lifetime}`);
    return parts.join(' · ');
  }

  function langBadge(languages) {
    if (!languages || languages.length <= 1) return '';
    return `<span class="lang-badge">🌐 ${languages.map(l => l.toUpperCase().split('-')[0]).join(' · ')}</span>`;
  }

  function renderWebCard(app, lang) {
    const isFeatured = app.type === 'featured';
    const name = lang === 'en' && app.nameEn ? app.nameEn : app.name;
    const desc = lang === 'en' && app.descEn ? app.descEn : app.desc;
    const hint = lang === 'en' && app.hintEn ? app.hintEn : app.hint;
    const label = app.featuredLabel ? app.featuredLabel.replace('\n', '<br>') : '';
    const visual = isFeatured
      ? `<div class="card-visual card-visual--wide" style="background:linear-gradient(${app.gradient});"><span style="font-family:'JetBrains Mono',monospace;font-size:0.9rem;font-weight:700;color:white;text-align:center;line-height:1.3;">${label}</span></div>`
      : `<div class="card-visual" style="background:linear-gradient(${app.gradient});border:none;"><span style="font-size:1.5rem;">${app.icon}</span></div>`;
    return `<a class="card ${isFeatured ? 'card--featured' : 'card--row'}" href="${app.url}" target="_blank">${visual}<div class="card-info"><span class="card-badge badge--${app.badgeColor}">${app.badge}</span><h3 class="card-title">${name}</h3><p class="card-desc">${desc}</p><div class="card-hint">&rarr; ${hint}</div></div></a>`;
  }

  function renderIosCard(app, iconUrl, lang) {
    const visual = iconUrl
      ? `<div class="card-visual" style="border:none;background:none;"><img src="${iconUrl}" alt="${app.name}"></div>`
      : `<div class="card-visual" style="background:linear-gradient(135deg,#888,#444);border:none;"><span style="font-size:1.5rem;">📱</span></div>`;
    const desc = lang === 'en' && app.descEn ? app.descEn : app.desc;
    return `<a class="card card--row" href="${app.landingURL}" target="_blank">${visual}<div class="card-info"><span class="card-badge badge--${app.badgeColor}">${app.badge}</span><h3 class="card-title">${app.name}</h3><p class="card-desc">${desc}</p><div class="card-hint">${pricingHint(app.pricing, lang)} &rarr;${langBadge(app.languages)}</div></div></a>`;
  }

  function renderMacosCard(app, lang) {
    const url = app.githubURL || app.landingURL || '#';
    const label = app.featuredLabel ? app.featuredLabel.replace('\n', '<br>') : app.name;
    const name = lang === 'en' && app.nameEn ? app.nameEn : app.name;
    const desc = lang === 'en' && app.descEn ? app.descEn : app.desc;
    const hint = lang === 'en' && app.hintEn ? app.hintEn : app.hint;
    return `<a class="card card--featured" href="${url}" target="_blank"><div class="card-visual card-visual--wide" style="background:linear-gradient(${app.gradient});"><span style="font-family:'JetBrains Mono',monospace;font-size:0.9rem;font-weight:700;color:white;text-align:center;line-height:1.3;">${label}</span></div><div class="card-info"><span class="card-badge badge--${app.badgeColor}">${app.badge}</span><h3 class="card-title">${name}</h3><p class="card-desc">${desc}</p><div class="card-hint">&rarr; ${hint}</div></div></a>`;
  }

